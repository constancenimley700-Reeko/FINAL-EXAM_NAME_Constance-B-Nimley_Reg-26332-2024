---Farmers Table
CREATE TABLE Farmers (
    Farmer_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    First_Name VARCHAR2(50) NOT NULL,
    Last_Name VARCHAR2(50) NOT NULL,
    Phone_Number VARCHAR2(15) UNIQUE,
    District VARCHAR2(50) NOT NULL,
    Registration_Date DATE DEFAULT SYSDATE
);

--Batches Table (For Grouping Deliveries)
CREATE TABLE Batches (
    Batch_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Batch_Name VARCHAR2(50) NOT NULL,
    Created_Date DATE DEFAULT SYSDATE
);

--Deliveries Table (Tracks incoming coffee cherries)
CREATE TABLE Deliveries (
    Delivery_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Farmer_ID NUMBER REFERENCES Farmers(Farmer_ID) ON DELETE CASCADE,
    Batch_ID NUMBER REFERENCES Batches(Batch_ID),
    Weight_Kg NUMBER(10,2) NOT NULL CONSTRAINT check_positive_weight CHECK (Weight_Kg > 0),
    Quality_Grade CHAR(1) CONSTRAINT check_grade CHECK (Quality_Grade IN ('A', 'B', 'C')),
    Delivery_Date DATE DEFAULT SYSDATE
);

---Processing Table (Tracks milling stages)
CREATE TABLE Processing (
    Process_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Batch_ID NUMBER REFERENCES Batches(Batch_ID),
    Stage VARCHAR2(30) CONSTRAINT check_stage CHECK (Stage IN ('Washing', 'Drying', 'Milling', 'Ready')),
    Status VARCHAR2(20) DEFAULT 'Pending',
    Last_Updated DATE DEFAULT SYSDATE
);

---Payouts Table (Calculates financial payments to farmers)
CREATE TABLE Payouts (
    Payout_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Delivery_ID NUMBER REFERENCES Deliveries(Delivery_ID) ON DELETE CASCADE,
    Amount_RWF NUMBER(12,2) NOT NULL,
    Payment_Status VARCHAR2(20) DEFAULT 'Pending' CONSTRAINT check_payout_status CHECK (Payment_Status IN ('Pending', 'Approved', 'Paid')),
    Payout_Date DATE
);

--Public Holidays Reference Table (For temporal restriction triggers)
CREATE TABLE Public_Holidays (
    Holiday_Date DATE PRIMARY KEY,
    Holiday_Name VARCHAR2(100) NOT NULL
);


--Audit Log Table (For security/history tracking)

CREATE TABLE Audit_Log (
    Audit_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Table_Name VARCHAR2(50),
    Action VARCHAR2(10),
    User_Executed VARCHAR2(50),
    Action_Timestamp TIMESTAMP DEFAULT SYSTIMESTAMP,
    Details VARCHAR2(4000)
);


