-----Temporal Constraints Trigger--
--System Security Trigger
CREATE OR REPLACE TRIGGER trg_restrict_operations
BEFORE INSERT OR UPDATE OR DELETE ON Payouts
DECLARE
    v_day VARCHAR2(10);
    v_is_holiday NUMBER;
BEGIN
    -- Extract the day of the week
    v_day := TO_CHAR(SYSDATE, 'DY', 'NLS_DATE_LANGUAGE=ENGLISH');
    
    -- Check if today is registered in our Holiday table
    SELECT COUNT(*) INTO v_is_holiday 
    FROM Public_Holidays 
    WHERE TRUNC(Holiday_Date) = TRUNC(SYSDATE);

    -- Block transactions on Weekends OR Public Holidays
    IF v_day IN ('SAT', 'SUN') OR v_is_holiday > 0 THEN
        raise_application_error(-20001, 'Unauthorized operation: Database modifications are blocked on weekends and public holidays.');
    END IF;
END;
/

---Audit Trail Trigger (User Activity Tracking)--
CREATE OR REPLACE TRIGGER trg_audit_payout_updates
AFTER UPDATE OF Payment_Status ON Payouts
FOR EACH ROW
BEGIN
    INSERT INTO Audit_Log (Table_Name, Action, User_Executed, Details)
    VALUES (
        'PAYOUTS',
        'UPDATE',
        USER,
        'Payout ID ' || :OLD.Payout_ID || ' status changed from ' || :OLD.Payment_Status || ' to ' || :NEW.Payment_Status
    );
END;
/

